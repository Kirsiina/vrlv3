<p>Virtuaalisen ratsastajainliiton jäsenyys, eli VRL-tunnus, on tarkoitettu virtuaalihevosista ja liiton toiminnasta kiinnostuneille henkilöillä iästä riippumatta. Jokainen jäsen saa yksilöllisen tunnuksen, joka toimii henkilötunnuksen tavoin virtuaalimaailmassa.</p>

<p><b>Rekisteröitymällä jäseneksi voit mm.</b><br>
-Lähettää pikaviestejä kavereiden kanssa, ja osallistua VRL:n päätöksentekoon.<br>
-Rekisteröidä hevosia, kasvattajanimiä, talleja ja seuroja, ja muokata näiden tietoja reaaliaikaisesti.<br>
- Osallistua kilpailuihin ja näyttelyihin, ja kantakirjata hevosia.</p>

<p>Tunnukset ovat henkilökohtaisia<br>
- Kukin henkilö on oikeutettu vain yhteen VRL-tunnukseen väärinkäytösten välttämiseksi! Jos olet unohtanut salasanasi tai tunnuksesi, älä rekisteröi uutta tunnusta, vaan palauta salasana tai ota yhteyttä ylläpitoon.</p>
