<h1>Tietosuojaseloste</h1>


<p>Virtuaalinen Ratsastajainliitto kerää jäsenistään hyvin vähän henkilötietoa, sillä harrastuksessa esiinnytään vain nimimerkillä ja VRL-tunnuksella,
ja esimerkiksi kaikki hevoset, tallit ja tapahtumat ovat keksittyjä eikä niillä ole ainakaan tarkoituksellista yhteyttä reaalimaailmaan.</p>

<p>VRL kerää käyttäjistään <b>sähköpostiosoitteen</b>, jota käytetään pääasiassa autentikoinnin vahvistamiseen ja (käyttäjän niin halutessa)
yhteydenottotapana harrastajien välillä. Käyttäjä voi asettaa profiiliinsa näkyviin myös muita yhteystietojaan, tai vapaasti poistaa niitä.
Mitään yhteystietoja ei käytetä markkinointiin, eikä luovuteta kolmansille osapuolille.</p>

<p>Sähköpostin lisäksi sisäänkirjautumisista tallennetaan kirjautuvan käyttäjän <b>IP-osoite</b> tietoturvaan liittyvistä syistä.
VRL käyttää myös <b>evästeitä</b> autentikaatiota varten, mutta
niitä ei hyödynnetä käyttäjän toimien seuraamiseen tai minkäänlaisen markkinoinnin kohdentamiseen.  </p>

<p>Mikäli olet huolissasi tietosuojastasi VRL:n yhteydessä, olethan yhteydessä liiton ylläpitoon!</p>
