<h1>Alayhdistykset</h1>

<p>VRL:n alaisuudessa toimii useita erilaisia alayhdistyksiä, esimerkiksi kilpailujaoksia
ja näiden laatuarvosteluita, muita laatuarvosteluita, kantakirjoja ja rotuyhdistyksiä. </p>

<p>Tältä sivulta voit selata alayhdistyksiä ja tapahtuma-arkistoa. Mikäli haluat oman yhdistyksesi VRL:n alaisuuteen, ole yhteydessä ylläpitoon!</p>