
<h3>Käyttöohje</h3>
<p>Valitse emälle ja isälle värit taulukosta. Jos et ole varma yhdistelmistä, kokeile erilaisia. Ruksattujen kohtien muodostaman värin voi lukea taulukon yläreunasta, ja kokeilemalla löydät oikeat värit vanhemmille. Esimerkiksi ruksaamalla ruunikon ja voikon, muodostuu ruunivoikko ja lisäämällä esimerkiksi ruksin hallakkoon, saat ruunivoikonhallakon. 
</p><p>
Klikkaa "generoi varsan värit".</p><p>

Nyt saat varsalle varmat värivaihtoehdot(max. 3), sekä mahdollisuuden ruksata lisää ruutuja. Ruutuja ruksailemalla voit muodostaa kaikki varsalle mahdolliset värit varmoihin väreihin yhdistettynä.
</p>



<h3>FAQ</h3>
<p><b>*Generaattori ei muodosta liinakkoa?</b><br>
Liinakko on vaaleajouhinen rautias. Generaattori ei huomioi sävyjä tai jouhien väritystä, sillä niiden periytyminen on monimutkaista.
</p>

<p><b>*Valitsemalla yhdistelmän voikko ja rautias, muodostuu pelkkä voikko. Onko tämä oikein?</b><br>
Suomen kielessä voikko tarkoittaa rautiaspohjaista voikkoa. On siis olemassa käsite ruunivoikko, mutta "rautiaanvoikko" on pelkkä voikko. Jos hevosesi on siis "pelkkä" voikko, merkitse sen pohjaväriksi rautias.
</p>
<p><b>*Vaikka klikkaan ruksia "hallakko/hiirakko" ei silti muodostu väriä hiirakko!</b><br>
Jos hevosesi on hiirakko, laita sille pohjaväriksi musta. Sitten näkyy.
</p>
<p><b>*Listassa ei ole mahdollista ruksata cremelloa/perlinoa/smoky white?</b><br>
Nämä on kaikki yhdistetty "tuplavoikko" ruksin alle, ja näkyvät kyllä muodostuneessa värinimessä.
</p>
