<h2>Virtuaalinen ratsastajainliitto</h2>
<p>Virtuaalinen ratsastajainliitto eli VRL on virtuaalihevosten ja -tallien harrastajien keskusjärjestö ja kohtaamispaikka. Liitto on toiminut vuodesta 1999 tavoitteenaan yhtenäistää ja kehittää virtuaalimaailmaa, opastaa niin uusia kuin vanhojakin harrastajia ja tiedottaa ajankohtaisista asioista.</p>
<p>Toimintaan on helppo tulla mukaan. Ensikertalaisen kannattaa aloittaa VirtuaaliWikiin, joka tutustuttaa käsitteisiin ja käytäntöihin. Jos olet jo harrastaja, saat VRL:stä eniten irti rekisteröimällä itsellesi VRL-tunnuksen, jolla pääset kirjautumaan sisään sivuille. Voit jatkaa tutustumista esimerkiksi ilmoittamalla virtuaalitallisi tallilistalle, rekisteröimällä hevosesi tai osallistumalla kilpailuihin.
Tutustu myös moniin liiton alaisiin järjestöihin ja yhdistyksiin, jotka järjestävät erilaista toimintaa virtuaalimaailmassa.</p>

