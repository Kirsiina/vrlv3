<p>Tapahtumat ovat jaoksen alaisia laatuarvosteluja ja muita palkitsemistilaisuuksia.
Kun tapahtuma kirjataan VRL:n tietokantaan, siitä saadut palkinnot näkyvät hevosen rekisteriprofiilissa.
Jaokset voivat pitää tapahtumansa vapaasti haluamallaan tavalla omilla sivuillaan. Tulokset ilmoitetaan VRL:oon jälkikäteen.
Myös vanhoja tapahtumia ja tuloksia voi kirjata samaan tapaan. </p>