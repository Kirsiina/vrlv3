<h1>Värien periytyminen</h1>

<p>Virtuaalihevosten värit eivät periydy automaattisesti, ja hevonen voi myös vaihtaa väriä elämänsä aikana.
Värien periytyminen onkin kiinni virtuaalihevosen omistajan viitseliäisyydestä, eikä siksi kovin yleistä.
Harrastajat ovat kuitenkin enenevässä määrin kiinnostuneita värien periytymisestä, ja siksi VRL tarjoaa työkaluja tätä varten.
</p>

<p>Värien periytyminen alkaa siitä, että olemassa olevien hevosten värit osaa tunnistaa.
Mikäli värien periytyminen ei ole tuttua, turvallisin vaihtoehto on valita varsalle sama väri kuin jommalla kummalla vanhemmalla on.
Tämä sääntö pätee lähes kaikkien yleisimpien värien kohdalla lukuun ottamatta tuplavoikkoja. Voit varmistaa valintasi VRL:n perinnöllisyyslaskurilla.</p>